package StringPrograms;

public class GetCharAtIndex {
    public static void main(String[] args) {
        String str="Java Programs!";

        int index1=0;
        int index2=10;

        char ch1= str.charAt(index1);
        char ch2= str.charAt(index2);

        System.out.println("Index present at "+ index1+ " position is "+ch1);
        System.out.println("Index present at"+ index2+ "position is "+ ch2);
    }



}
